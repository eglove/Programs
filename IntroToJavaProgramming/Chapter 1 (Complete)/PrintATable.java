public class PrintATable {
	public static void main(String[] args) {
		System.out.println("a\ta^2\ta^3");
		System.out.println("1\t" + Math.pow(1,2) + "\t" + Math.pow(1,3));
		System.out.println("2\t" + Math.pow(2,2) + "\t" + Math.pow(2,3));
		System.out.println("3\t" + Math.pow(3,2) + "\t" + Math.pow(3,3));
		System.out.println("4\t" + Math.pow(4,2) + "\t" + Math.pow(4,3));
	}
}